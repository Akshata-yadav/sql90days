---Find total revenue generated by each restaurant
select restaurant_name, sum(order_value) as revenue_generated from april11
group by restaurant_name;

---Find average delivery time per restaurant
select * from april11;

select restaurant_name , avg(delivery_time) as avg_delivery_time from april11
group by restaurant_name;

---Find number of orders per payment method
select payment_method, count(order_id) as number_orders from april11
group by payment_method;

---Find the highest order value per customer
select * from april11;

select customer_id ,max(order_value) as highest_order_value from april11
group by customer_id;

---Find customers who placed more than 2 orders
select * from april11;

select customer_id ,count(*) as order_count from april11
group by customer_id 
