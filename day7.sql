---Rank restaurants based on total revenue
select * from april11;

select restaurant_name , sum(order_value) as total_revenue ,rank() over
(order by sum(order_value) desc) as rnk from april11
group by restaurant_name;

---Find customer retention (customers ordering again within 3 days)
select * from april11;

WITH orders_lag AS (
    SELECT 
        customer_id,
        order_date,
        LAG(order_date) OVER (PARTITION BY customer_id ORDER BY order_date) AS prev_order
    FROM april11
)
SELECT DISTINCT customer_id
FROM orders_lag
WHERE DATEDIFF(day, prev_order, order_date) <= 3;

---Find peak order day (highest number of orders)
select * from april11;

select top 1 order_date, count(*) as order_count from april11
group by order_date 
order by  count(*) desc;

